import numpy as np
import tensorflow as tf
import tensorflow.contrib.lite as tflite
from keras.utils import np_utils
np.random.seed(1671) # for reproducibility
# network and training
NB_EPOCH = 200
BATCH_SIZE = 128
VERBOSE = 1
NB_CLASSES = 10 # number of outputs = number of digits
OPTIMIZER = tf.keras.optimizers.SGD() # SGD optimizer, explained later in this chapter
N_HIDDEN = 128
VALIDATION_SPLIT=0.2 # how much TRAIN is reserved for VALIDATION
# data: shuffled and split between train and test sets
X_train = []
Y_train = []

with open("C:/Users/salzm/Documents/hslu/Semester 5/MOBLAB/Projekt/dataset.txt", "r", encoding="utf-8") as dataset:
    for line in dataset.readlines():
        label = line.split(":")[0]
        datasetAsString = line.split(":")[1]
        dataAsString = datasetAsString.split(",")
        data = []
        for dataString in dataAsString:
            data.append(float(dataString))
        Y_train.append(label)
        X_train.append(data)

# convert to numpy array
X_train = np.asarray(X_train)
Y_train = np.asarray(Y_train)

#X_train is len(X_train) rows of 28x28 values --> reshaped in len(X_train) x 784
RESHAPED = 784
X_train = X_train.reshape(len(X_train), RESHAPED)

print(X_train.shape[0], 'train samples')
# convert class vectors to binary class matrices
Y_train = np_utils.to_categorical(Y_train, NB_CLASSES)

print(Y_train)

def createModel():
    # 10 outputs
    # final stage is softmax
    model = tf.keras.models.Sequential()
    model.add(tf.keras.layers.Dense(NB_CLASSES, input_shape=(RESHAPED,)))
    model.add(tf.keras.layers.Activation('softmax'))
    model.summary()
    model.compile(loss=tf.keras.losses.categorical_crossentropy, optimizer=OPTIMIZER, metrics=[tf.keras.metrics.categorical_accuracy])
    return model

def train(model):
    # train
    model.fit(X_train, Y_train, batch_size=BATCH_SIZE, epochs=NB_EPOCH, verbose=VERBOSE, validation_split=VALIDATION_SPLIT)
    return model

